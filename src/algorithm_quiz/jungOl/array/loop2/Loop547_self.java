package algorithm_quiz.jungOl.array.loop2;

public class Loop547_self {
    public static void main(String[] args) {

        for (int i = 2; i <= 6; i++) {
            for (int j = i; j < i + 5; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }


    }
}
