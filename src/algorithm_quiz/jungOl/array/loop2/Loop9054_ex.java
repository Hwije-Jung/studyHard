package algorithm_quiz.jungOl.array.loop2;

public class Loop9054_ex {
    public static void main(String[] args) {
        for(int i=1; i<=9 ; i++){
            for(int j =2 ; j<=4; j++){
                System.out.printf("%d * %d = %2d   ",j,i,j*i);
            }
            System.out.println();
        }
    }
}
