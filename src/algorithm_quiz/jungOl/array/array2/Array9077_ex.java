package algorithm_quiz.jungOl.array.array2;

public class Array9077_ex {
    public static void main(String[] args) {
        int[][] numbers = {{3,5,4},{2,6,7},{8,10,1}};

        for(int i = 0 ; i< 3; i++){
            for(int j = 0 ; j<3 ; j++){
                System.out.print(numbers[i][j]+" ");
            }
            System.out.println();
        }

    }
}
